// ── Author-Query auf Frontend abfangen ──
add_action( 'template_redirect', function() {
    if ( is_admin() ) return;

    // /?author=N — Standard-Probe-URL
    if ( isset( $_GET['author'] ) ) {
        wp_safe_redirect( home_url( '/' ), 301 );
        exit;
    }

    // /author/<name>/ — wenn Permalinks aktiv
    if ( is_author() ) {
        wp_safe_redirect( home_url( '/' ), 301 );
        exit;
    }
}, 1 );

