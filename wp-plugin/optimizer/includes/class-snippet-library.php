<?php
/**
 * WFOCO_Snippet_Library — die 5 kuratierten Performance-Fixes als embedded
 * PHP-Strings. Quelle-of-Truth ist src/lib/smartfix-snippets.ts auf
 * website-fix.com; diese PHP-Variante ist 1:1 portiert mit identischen
 * Safety-Checks und Body-Code.
 *
 * Jeder Snippet liefert:
 *   - slug:          stabiler URL-/Datei-Slug
 *   - title:         Anzeige-Name im Admin
 *   - tagline:       1-Satz-Was-tut-es
 *   - problem_tag:   Pill-Label (z.B. "Hoher TTFB · CPU-Last")
 *   - effect:        Erwarteter Effekt in 1-2 Sätzen
 *   - hoster_scope:  "Alle Hoster" / "Mit Cloudflare empfohlen" etc.
 *   - effect_scope:  "Frontend" / "Backend" / "Beides"
 *   - warning:       (optional) Kompat-Hinweis
 *   - code:          das vollständige PHP-Snippet, READY für mu-plugin-Datei
 *                    (inkl. <?php Header, ABSPATH-Check, Safety-Check, Body)
 *
 * Snippets werden mit dem `WebsiteFix One-Click Optimizer`-Marker im File-
 * Header geschrieben — das Uninstall-Script erkennt sie daran und löscht
 * NUR unsere Dateien (keine fremden mu-plugins).
 */

defined( 'ABSPATH' ) || exit;

class WFOCO_Snippet_Library {

    /**
     * Liefert das vollständige Snippet-Array.
     *
     * @return array<int, array<string, mixed>>
     */
    public static function get_all() {
        return array(
            self::snippet_heartbeat(),
            self::snippet_xmlrpc(),
            self::snippet_emojis(),
            self::snippet_query_strings(),
            self::snippet_jquery_migrate(),
            self::snippet_author_archive_block(),
            self::snippet_hide_wp_version(),
        );
    }

    /**
     * Lookup per slug — null wenn nicht vorhanden.
     */
    public static function get( $slug ) {
        foreach ( self::get_all() as $s ) {
            if ( $s['slug'] === $slug ) {
                return $s;
            }
        }
        return null;
    }

    /**
     * Wrappt den Snippet-Body in den standardisierten mu-plugin-Header
     * mit Marker, Datum und Versionierung. Wird beim Apply in die
     * mu-plugin-Datei geschrieben.
     */
    public static function build_file_content( $snippet ) {
        $now    = gmdate( 'Y-m-d H:i:s' );
        $marker = 'WebsiteFix One-Click Optimizer';
        $header = "<?php\n"
            . "/**\n"
            . " * Plugin Name: WebsiteFix Optimizer · " . $snippet['title'] . "\n"
            . " * Description: " . $snippet['tagline'] . "\n"
            . " * Version:     " . WFOCO_VERSION . "\n"
            . " *\n"
            . " * Auto-generated by {$marker} on {$now} UTC.\n"
            . " * Slug: " . $snippet['slug'] . "\n"
            . " *\n"
            . " * Diese Datei wird automatisch entfernt, wenn der entsprechende\n"
            . " * Fix im WebsiteFix-Optimizer-Settings deaktiviert oder das\n"
            . " * Plugin deinstalliert wird. NICHT manuell bearbeiten — Änderungen\n"
            . " * werden beim nächsten Re-Apply überschrieben.\n"
            . " */\n\n"
            . "defined( 'ABSPATH' ) || exit;\n\n";

        return $header . $snippet['code'];
    }

